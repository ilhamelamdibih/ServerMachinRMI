    private void listMachinesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listMachinesMouseClicked
        // TODO add your handling code here:
    int row = listMachines.getSelectedRow();

    Object value = listMachines.getValueAt(row, 0);
    
    int machineId = Integer.parseInt(value.toString()); // Assuming the value is an integer identifier
        try {
            Machine m = dao.findById(machineId);
            txtRef.setText(m.getRef());
            txtMarque.setText(m.getMarque());
            txtPrix.setText(Double.toString(m.getPrix()));
            comboBoxSalle.setSelectedItem(m.getSalle().getCode());
            idMachine = m.getId();
            
        } catch (RemoteException ex) {
            Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_listMachinesMouseClicked

    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Machine updateMachine = dao.findById(idMachine);
            updateMachine.setRef(txtRef.getText().toString());
            updateMachine.setMarque(txtMarque.getText().toString());
            updateMachine.setPrix(Double.parseDouble(txtPrix.getText().toString()));
            Salle salle= daos.findByCode(comboBoxSalle.getSelectedItem().toString());
            updateMachine.setSalle(salle);
            //updateMachine.setSalle();
            
            dao.update(updateMachine);

            load();
        } catch (RemoteException ex) {
            Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bnUpdateActionPerformed

