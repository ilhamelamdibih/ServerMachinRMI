    public void load(){
        try {
            idMachine = -1;
            model.setRowCount(0);
            for(Machine m : dao.findAll())
                model.addRow(new Object[]{
                    m.getId(),
                    m.getRef(),
                    m.getMarque(),
                    m.getPrix(),
                    m.getSalle().getCode()
                }); 
            txtRef.setText("");
            txtMarque.setText("");
            txtPrix.setText("");
            comboBoxSalle.setSelectedItem("");
        } catch (RemoteException ex) {
            Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void loadSalle()
    {
    try {

            for(Salle s : daos.findAll()){
                comboBoxSalle.addItem(s.getCode());
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
